package Main;

public class Main{
	public static void main(String[] args) {

		int i = 0;

		final int max = 10;

	

		while(i<max) {

		System.out.println(Integer.toString(i));

		i++;
		}
		
		i = 0;
		do {

		System.out.println(Integer.toString(i));

		i++;

		}while(i<max);
		

		for(i=0;i<max;i++) {

		System.out.println(Integer.toString(i));

		

		}

		}

		}
